package com.valerio.android.aplicacionprofequiz.Vista.modelo;

public class Perfil {

    private String cod_prof;
    private String nombre_completo;
    private String correo_prof;
    private String especial_prof;
    private String uni_prof;
    private String about_prof;
    private String curso_prof;
    private String meto_prof;
    private String calificacion;


    public String getCod_prof() {
        return cod_prof;
    }

    public void setCod_prof(String cod_prof) {
        this.cod_prof = cod_prof;
    }

    public String getNombre_completo() {
        return nombre_completo;
    }

    public void setNombre_completo(String nombre_completo) {
        this.nombre_completo = nombre_completo;
    }

    public String getCorreo_prof() {
        return correo_prof;
    }

    public void setCorreo_prof(String correo_prof) {
        this.correo_prof = correo_prof;
    }

    public String getEspecial_prof() {
        return especial_prof;
    }

    public void setEspecial_prof(String especial_prof) {
        this.especial_prof = especial_prof;
    }

    public String getUni_prof() {
        return uni_prof;
    }

    public void setUni_prof(String uni_prof) {
        this.uni_prof = uni_prof;
    }

    public String getAbout_prof() {
        return about_prof;
    }

    public void setAbout_prof(String about_prof) {
        this.about_prof = about_prof;
    }

    public String getCurso_prof() {
        return curso_prof;
    }

    public void setCurso_prof(String curso_prof) {
        this.curso_prof = curso_prof;
    }

    public String getMeto_prof() {
        return meto_prof;
    }

    public void setMeto_prof(String meto_prof) {
        this.meto_prof = meto_prof;
    }

    public String getCalificacion() {
        return calificacion;
    }

    public void setCalificacion(String calificacion) {
        this.calificacion = calificacion;
    }
}
