package com.valerio.android.aplicacionprofequiz.Vista;

public class Resena {

    private int cod_quiz;
    private int cod_user;
    private String cod_prof;
    private String calificacion;
    private String comentario;

    // Getters and setters
    public int getCodQuiz() { return cod_quiz; }
    public void setCodQuiz(int cod_quiz) { this.cod_quiz = cod_quiz; }

    public int getCodUser() { return cod_user; }
    public void setCodUser(int cod_user) { this.cod_user = cod_user; }

    public String getCodProf() { return cod_prof; }
    public void setCodProf(String cod_prof) { this.cod_prof = cod_prof; }

    public String getCalificacion() { return calificacion; }
    public void setCalificacion(String calificacion) { this.calificacion = calificacion; }

    public String getComentario() { return comentario; }
    public void setComentario(String comentario) { this.comentario = comentario; }




}
